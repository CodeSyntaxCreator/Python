# Random Number Program
#Importing random module
import random; #built-in module
#Declaring randomNum
randomNum = random.randrange(1,10); #from 1 to 10
print(randomNum)

#Random Float
randomFloat = random.random() #Generate a random float from 0 to 1
print(randomFloat)

#Random Intege
randomInteger = random.randint(10, 1000) # from 10 to 1000
print(randomInteger)